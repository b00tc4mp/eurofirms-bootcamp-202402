function salute(name, surname) {
  console.log("Hello " + name + " " + surname);
}

salute("Pepito", "Grillo");

function multiplica(a, b) {
  return a * b;
}

function divide(a, b) {
  return a / b;
}

function resta(a, b) {
  return a - b;
}

var valorMultiplicacion = multiplica(2, 2);
console.log(valorMultiplicacion);

var valorDivide = divide(2, 2);
console.log(valorDivide);

var valorResta = resta(2, 2);
console.log(valorResta);
